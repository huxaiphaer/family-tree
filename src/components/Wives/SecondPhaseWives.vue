<template>
    <div>
        <v-container align="center">
            <v-card
                    class="mx-auto my-12"
                    max-width="374"
                    align="center"
            >
                <v-img
                        height="250"
                        src="https://cdn.vuetifyjs.com/images/cards/cooking.png"
                ></v-img>
                <v-card-title>{{name}}</v-card-title>
            </v-card>
        </v-container>
        <v-container fill-height align="center">
            <v-row>
                <v-col cols="12">
                    <v-row
                            :align="center"
                            :justify="center"
                            class="grey lighten-5 mx-10 my-10"
                            style="height: 300px;"
                    >
                        <v-card
                                v-for="(n , i) in allData"
                                :key="n"
                                outlined
                                style="margin: 10px; align-items: center"
                                tile
                                elevation="30"
                                v-on:click="navigateToNextPage($event,i, n)"
                        >
                            <v-img
                                    height="250"
                                    src="https://cdn.vuetifyjs.com/images/cards/cooking.png"
                            ></v-img>
                            <v-card-title><b>{{n.Name}}</b></v-card-title>
                        </v-card>
                    </v-row>
                </v-col>
            </v-row>
        </v-container>
    </div>
</template>

<script>
    import {db} from "../../firebase/db";
    import router from "../../router";

    let export_path = ''

    export default {
        name: "SecondPhaseWives",
        props: ['name', 'photo', 'number', 'path'],
        data() {
            return {
                allData: {},
            }
        },
        mounted() {
            this.$rtdbBind('allData', db.ref(this.path))

            export_path = this.path
        },
        methods: {
            navigateToNextPage(ev, i, n) {

                let formatPath = db.ref(export_path).ref.toString().substr(41)

                if (n.c.toString().trim() === 'Yes') {

                    router.push({
                        name: 'ThirdPhaseChildren', params:
                            {
                                name: n.Name,
                                photo: '',
                                number: i,
                                path: formatPath + '/'+ i+ '/Children',
                            }
                    })
                } else if (n.w.toString().trim() === 'Yes') {

                    let compute = i + 1;
                    let eachPersonNumber = '00' + compute;
                    router.push({name: 'ThirdPhaseWives', params:
                            {   name: n.Name,
                                photo: '',
                                number: eachPersonNumber,
                                path:formatPath + '/' + eachPersonNumber + '/wives',
                            }})
                } else {
                    // eslint-disable-next-line no-console
                    this.$noty.error("Sorry, this person has no thread yet.")
                }
            },
        }
    }
</script>

